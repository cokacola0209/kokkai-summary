  // ──────────────────────────────────────────
  // 正規化
  // ──────────────────────────────────────────

  /**
   * speaker 文字列を正規化する。
   *
   * 処理:
   *  1. 先頭の○マーク除去
   *  2. 全角/半角スペース除去
   *  3. 末尾の役職サフィックス除去（長い順にマッチ）
   *  4. trim
   */
  export function normalizeSpeakerName(raw: string): string {
    let name = raw
      .replace(/^[○〇]\s*/, "")    // 先頭の○マーク除去
      .replace(/[\s\u3000]+/g, "") // 全角・半角スペース除去
      .trim();

    // 末尾の役職サフィックスを除去（長い順にマッチ）
    for (const suffix of TITLE_SUFFIXES) {
      if (name.endsWith(suffix)) {
        name = name.slice(0, -suffix.length);
        break;
      }
    }

    return name.trim();
  }

  // ──────────────────────────────────────────
  // バリデーション
  // ──────────────────────────────────────────

  /**
   * 正規化後の名前が有効な人名かどうか判定する。
   */
  export function isValidPersonName(name: string): boolean {
    if (!name) return false;
    if (name.length < 2 || name.length > 10) return false;

    // ブロックワードを含む
    if (BLOCKED_WORDS.some((word) => name.includes(word))) return false;

    // 漢字・ひらがな・カタカナのみで構成されているか
    if (!/^[一-龯々ぁ-んァ-ヶー]+$/.test(name)) return false;

    return true;
  }
